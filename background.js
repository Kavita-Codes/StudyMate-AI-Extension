// CONTEXT MENU CLICK EVENTS
// ----------------------------------------------------
chrome.contextMenus.onClicked.addListener((info, tab) => {
    if (!tab || !tab.id) return;
    
    const selectedText = info.selectionText;
    const action = info.menuItemId;
    
    // 1. Direct local action: Save Highlight
    if (action === "save_highlight") {
        chrome.storage.local.get({ highlights: [] }, (result) => {
            const highlights = result.highlights;
            const exists = highlights.some(h => h.text.trim() === selectedText.trim());
            if (!exists) {
                highlights.push({
                    text: selectedText,
                    url: tab.url || "",
                    title: tab.title || "Web Page",
                    timestamp: Date.now()
                });
                chrome.storage.local.set({ highlights }, () => {
                    chrome.tabs.sendMessage(tab.id, { 
                        type: "SHOW_TOAST", 
                        message: "Highlight saved successfully!" 
                    });
                });
            } else {
                chrome.tabs.sendMessage(tab.id, { 
                    type: "SHOW_TOAST", 
                    message: "Highlight is already saved!" 
                });
            }
        });
        return;
    }
    
    // 2. Open overlay UI on page for response displaying
    chrome.tabs.sendMessage(tab.id, {
        type: "OPEN_OVERLAY",
        action: action,
        selection: selectedText
    }, () => {
        // Suppress errors for pages where scripts cannot run
        if (chrome.runtime.lastError) {
            console.warn("Unable to inject content script overlay on this tab:", chrome.runtime.lastError.message);
        }
    });
    
    // If user clicked "Ask AI...", the UI will wait for user input. We don't trigger the API yet.
    if (action === "ask_ai") {
        return;
    }
    
    // 3. For AI actions, call API and stream/send response back
    chrome.storage.sync.get(["geminiApiKey", "model"], async (settings) => {
        try {
            if (!settings.geminiApiKey) {
                chrome.tabs.sendMessage(tab.id, {
                    type: "UPDATE_OVERLAY",
                    success: false,
                    result: "API Key not found. Please open options to set your Gemini API Key."
                });
                return;
            }
            
            const resultText = await AIService.generate(selectedText, action, "", settings);
            
            chrome.tabs.sendMessage(tab.id, {
                type: "UPDATE_OVERLAY",
                success: true,
                result: resultText
            });
        } catch (error) {
            chrome.tabs.sendMessage(tab.id, {
                type: "UPDATE_OVERLAY",
                success: false,
                result: `Error: ${error.message}`
            });
        }
    });
});

// ----------------------------------------------------
// RUNTIME MESSAGES HANDLER
// ----------------------------------------------------
chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
    if (message.type === "GENERATE_AI") {
        const { text, action, customPrompt } = message;
        
        chrome.storage.sync.get(["geminiApiKey", "model"], (settings) => {
            if (!settings.geminiApiKey) {
                sendResponse({ success: false, error: "API Key not found. Please open settings and add your API Key." });
                return;
            }
            
            AIService.generate(text, action, customPrompt, settings)
                .then(result => {
                    sendResponse({ success: true, result });
                })
                .catch(error => {
                    sendResponse({ success: false, error: error.message });
                });
        });
        
        return true; // Keep channel open for async response
    }
    
    if (message.type === "SAVE_HIGHLIGHT_DIRECT") {
        const { text, url, title } = message;
        chrome.storage.local.get({ highlights: [] }, (result) => {
            const highlights = result.highlights;
            const exists = highlights.some(h => h.text.trim() === text.trim());
            if (!exists) {
                highlights.push({
                    text: text,
                    url: url || "",
                    title: title || "Saved Snippet",
                    timestamp: Date.now()
                });
                chrome.storage.local.set({ highlights }, () => {
                    sendResponse({ success: true, message: "Highlight saved!" });
                });
            } else {
                sendResponse({ success: false, error: "Already saved" });
            }
        });
        return true;
    }
});