// To add other providers (OpenAI, Claude, etc.), simply update this service or callGeminiAPI.
async function callGeminiAPI(prompt, apiKey, model = "gemini-2.5-flash") {
    // Try both v1beta and v1 endpoints to avoid version mismatch 404 errors
    const apiVersions = ["v1beta", "v1"];
    let lastError = null;

    for (const version of apiVersions) {
        try {
            const url = `https://generativelanguage.googleapis.com/${version}/models/${model}:generateContent?key=${apiKey}`;
            
            const response = await fetch(url, {
                method: "POST",
                headers: {
                    "Content-Type": "application/json"
                },
                body: JSON.stringify({
                    contents: [
                        {
                            parts: [
                                {
                                    text: prompt
                                }
                            ]
                        }
                    ]
                })
            });

            if (response.ok) {
                const data = await response.json();
                const candidateText = data.candidates?.[0]?.content?.parts?.[0]?.text;
                if (!candidateText) {
                    throw new Error("No response generated by the AI model.");
                }
                return candidateText;
            }

            const errorData = await response.json().catch(() => ({}));
            const errorMessage = errorData.error?.message || `HTTP error! status: ${response.status}`;
            lastError = new Error(errorMessage);

            // If it's a 404 (model not found) or similar, we retry the other API version
            if (response.status === 404 || errorMessage.toLowerCase().includes("not found") || errorMessage.toLowerCase().includes("not supported")) {
                console.warn(`Gemini API version ${version} failed for model ${model}. Retrying other versions...`);
                continue;
            } else {
                // If it's a terminal error (like bad API key or quota limit), throw immediately
                throw lastError;
            }
        } catch (err) {
            lastError = err;
            // Throw immediately if it's a terminal error
            if (err.message && (err.message.includes("API Key") || err.message.includes("blocked") || err.message.includes("No response"))) {
                throw err;
            }
        }
    }
    
    throw lastError || new Error(`Generation failed: Model ${model} not found or not supported.`);
}

const AIService = {
    async generate(text, action, customPrompt = "", settings = {}) {
        const apiKey = settings.geminiApiKey;
        if (!apiKey) {
            throw new Error("API Key not found. Please set your Gemini API Key in the Extension Settings.");
        }
        
        const model = settings.model || "gemini-2.5-flash";
        
        let prompt = "";
        switch (action) {
            // Smart Summary
            case "summary_short":
                prompt = `Summarize the following text in a concise paragraph of 2-3 sentences:\n\n${text}`;
                break;
            case "summary_bullet":
                prompt = `Provide a bulleted list summarizing the key takeaways from the following text:\n\n${text}`;
                break;
            case "summary_keypoints":
                prompt = `Extract the most important key points and concepts from the following text:\n\n${text}`;
                break;
            case "summary_exam":
                prompt = `Create comprehensive study/exam notes from the following text, highlighting important terms, definitions, formulas, and concepts in structured sections:\n\n${text}`;
                break;
            case "summary_linkedin":
                prompt = `Generate an engaging, professional LinkedIn post summarizing the core ideas from the following text. Include 3-5 relevant hashtags and emojis for readability:\n\n${text}`;
                break;
                
            // Explain Text
            case "explain_simple":
                prompt = `Explain the following text in simple, clear, and plain English, as if explaining to someone completely unfamiliar with the topic:\n\n${text}`;
                break;
            case "explain_hinglish":
                prompt = `Explain the following text in Hinglish (a natural, conversational mixture of Hindi and English written in Latin script) to make it highly relatable and simple:\n\n${text}`;
                break;
            case "explain_beginner":
                prompt = `Explain the following text at a beginner level, explaining basic terms, underlying rules, and concepts simply:\n\n${text}`;
                break;
            case "explain_professional":
                prompt = `Provide a professional, sophisticated, and industry-standard explanation of the following text, using correct terminology and business context:\n\n${text}`;
                break;
            case "explain_examples":
                prompt = `Explain the following text using clear, real-life analogies, practical examples, and everyday scenarios:\n\n${text}`;
                break;
                
            // Translate
            case "translate_hindi":
                prompt = `Translate the following text to Hindi. Ensure the translation is contextually natural, accurate, and preserves the tone:\n\n${text}`;
                break;
            case "translate_english":
                prompt = `Translate the following text to English. Ensure the translation is contextually natural, accurate, and preserves the tone:\n\n${text}`;
                break;
            case "translate_french":
                prompt = `Translate the following text to French. Ensure the translation is contextually natural, accurate, and preserves the tone:\n\n${text}`;
                break;
            case "translate_german":
                prompt = `Translate the following text to German. Ensure the translation is contextually natural, accurate, and preserves the tone:\n\n${text}`;
                break;
            case "translate_japanese":
                prompt = `Translate the following text to Japanese. Ensure the translation is contextually natural, accurate, and preserves the tone:\n\n${text}`;
                break;
                
            // Other Generators
            case "generate_notes":
                prompt = `Generate detailed, well-structured, and comprehensive learning notes from the following text. Use headers, bold terms, list items, and key callouts:\n\n${text}`;
                break;
            case "generate_flashcards":
                prompt = `Generate a set of educational flashcards (Questions and Answers) in a clear Q&A format based on the following text. Try to generate at least 3-5 flashcards.
Output format:
Q1: [Question]
A1: [Answer]
Q2: [Question]
A2: [Answer]
...
Text:\n\n${text}`;
                break;
            case "generate_quiz":
                prompt = `Generate a multiple-choice quiz based on the following text. Provide 3-5 questions. 
For each question, list option A, B, C, and D, and clearly state the correct answer with a brief explanation.
Output format:
Question X: [Question text]
A) [Option A]
B) [Option B]
C) [Option C]
D) [Option D]
Correct Answer: [Letter]
Explanation: [Reasoning]
...
Text:\n\n${text}`;
                break;
                
            // Custom Prompt / Ask AI
            case "ask_ai":
                prompt = `Answer the following question or instruction: "${customPrompt}" based on this text:\n\n"${text}"`;
                break;
                
            default:
                prompt = `Analyze the following text and explain it:\n\n${text}`;
        }
        
        return await callGeminiAPI(prompt, apiKey, model);
    }
};

